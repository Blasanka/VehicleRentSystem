    private void RentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RentButtonActionPerformed
        try{
            //add selected row values to a collection type vector 'v'
            v = (Vector) rentsHandle.model.getDataVector().elementAt(RentOnHandVehicleTable.getSelectedRow());
            
            
            RentAVehicle rent = new RentAVehicle();
            rent.getAll(v);
            
            //When click rent button also want customer details to do the deal
            //to get customer details two buttons 1. for regular customers and other one for new customer.
            new CheckForCustomer().setVisible(true);
            //System.out.println(v.get(0));
            
        }catch(ArrayIndexOutOfBoundsException a){//If not selected one of rows and click rent button
            message = "Select a vehicle to rent";
            //If not selected row of table cannot rent because no way to get vehicle details.
            //create anonymous object to validate empty field
            //new Validation().notify(message);
            JOptionPane.showMessageDialog(null, message);
        }
        
        rentsHandle.setSelectedRow(v);
    }//GEN-LAST:event_RentButtonActionPerformed

